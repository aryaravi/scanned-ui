export function recentActivity7days (lang, count, documentRecentDays) {
  switch (lang) {
    case EN:
      return `We've received ${count} document(s) in the last ${documentRecentDays} days. - `
    case ES:
      return `Recibimos ${count} documento(s) en los últimos ${documentRecentDays} días. - `
    case PT:
      return `Recebemos ${count} documento(s) nos últimos ${documentRecentDays} dias. - `
    case ZH:
      return `内收到 ${count} 份文件我們在過去 ${documentRecentDays} 天。 - `
    case VI:
      return `Chúng tôi đã nhận được ${count} tài liệu trong ${documentRecentDays} ngày qua. - `
    case HI:
      return `Nou resevwa  ${count} dokiman, Nan dènye ${documentRecentDays} jou yo. - `
    default:
      return `We've received ${count} document(s) in the last ${documentRecentDays} days. - `
  }
}
